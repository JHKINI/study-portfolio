def 프레임_정보_작성(프레임_인덱스, 프레임_투구수, pins):
    global 더하는횟수 #외부변수 가져와서 사용하는 global
    if 프레임_투구수 == 0 and pins == 10: #첫번째투구에 쓰러트린 핀수가 10인경우
        더하는횟수[프레임_인덱스] = 2 #2를 더하는 횟수값에 저장하기

def 프레임_인덱스_관리(pins):
    global 프레임_인덱스
    global 프레임_투구수
    #print(프레임_인덱스, 프레임_투구수,pins)
    프레임_정보[프레임_인덱스][프레임_투구수] = pins
    if pins == 10 and 프레임_투구수 == 0:
        프레임_정보[프레임_인덱스][3] = 2
        프레임_인덱스 +=1
        프레임_정보[프레임_인덱스][4] += 10
        if 프레임_정보[프레임_인덱스][3]>0:
            프레임_정보[프레임_인덱스][3] -= 1

    elif 프레임_투구수 == 1 :
        프레임_쓰러뜨린총핀수 = sum(프레임_정보[프레임_인덱스])
        if 프레임_쓰러뜨린총핀수 == 10:
           프레임_정보[프레임_인덱스][3] = 1
           프레임_정보[프레임_인덱스][4] += 프레임_정보[프레임_인덱스][1]
       
        프레임_인덱스 += 1
        프레임_투구수 = 0
    else:
        프레임_투구수 += 1

def 계산가능(프레임_인덱스):
    if 프레임_정보[프레임_인덱스][3] == 0:
       return True
   
def 계산하기(프레임_인덱스):
    global 프레임당점수
    global 프레임_정보
    # [0],[1],[2]는 투구값, [4]는 추가점수이므로 그것만 합산
    프레임당점수[프레임_인덱스] = (
        프레임_정보[프레임_인덱스][0] +
        프레임_정보[프레임_인덱스][1] +
        프레임_정보[프레임_인덱스][2] +
        프레임_정보[프레임_인덱스][4]
    )
    return 프레임당점수
def 누적점수계산(프레임_인덱스):
    global 누적점수
    global 프레임당점수
    if 프레임_인덱스 == 0:
        누적점수[프레임_인덱스] = 프레임당점수[프레임_인덱스]
    else:
        누적점수[프레임_인덱스] = 누적점수[프레임_인덱스-1] + 프레임당점수[프레임_인덱스]
    return 누적점수
